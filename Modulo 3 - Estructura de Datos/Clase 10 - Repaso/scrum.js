/* Que es scrum? */
/* 
Scrum es una metodologia agil de trabajo que se utiliz aprincipalmente en el desarrollo de tecnologia
*/

/* 
Roles:
-Product Owner(PO): Es el responsable de definir los requisitos del proyecto y priorizar
el trabajo del product backlog. Actua como enlace entre los stakeholders y el equipo de desarrollo

-Scrum Master: Su principal funcion es asegurar que el equipo de desarrollo comprenda y aplique los principios de scrum.
Elimina obstaculos que puedan dificultar el desarrollo y facilita el proceso.

-Equipo de desarollo: El grupo que trabaja en la creacion del producto.
Son autoorganizados, y multifuncionales, y se compromenten a entregar un incremento del producto al final de cada sprint
*/

/* Artefactos de SCRUM:
-Product Backlog: Es una lista priorizada, de todas las funcionalidades, caracteristicas, mejoras y defectos
que deben ser implementadas en el producto. Es propiedad del product owner, y se actualiza constatemente.
-Sprint backlog: Un seleccion de elementos sacados del product backlog que el equipo se compromete a terminar
en el transcurso del sprint.
-Incremento del producto: Es el resultado dewl trabajo completado al final de cada sprint. Debe ser potencialmente entregable
Y aportar valor al producto.
*/


/* 
Eventos en scrum:
-Sprint: un periodo de tiempo fijo (generalmente entre 1 y 4 semanas)durante el cual el equipo de desarrollo
trabaja en la implementacion de elementos del product backlog

-Sprint Planning: El equipo selecciona elementos del product backlog para trabajar durante el sprint y crear el
sprint backlog

-Daily Scrum: Una breve reunion diaria en la que el equipo actualiza los progresos y se habla sobre psibles
dificultades y obstaculos

-Sprint Review: al final de cada sprint el equipo de desarrollo presenta el incremento del producto
y recibe feedback de los stakeholders

-Sprint Retro: El equipo reflexiona sobre el desempe√±o durante el sprint y busca maneras de mejorar.
*/

/* Stakeholders: Interesados en el proyecto(Basicamente el cliente o el que pone la $$ para el proyecto) 
MVP: Minimum Viable product, es el producto minimo que es necesario completar para que sea el producto en si.
*/

/* 
Principios de scrum:
-Transparencia: Todas las partes del proyecto deben ser visibles y entendidas por todos.
-Inspeccion: Se deben realizar revisiones regulares para detectar problemas y oportunidades de mejora.
-Adaptacion: Basandose en la inspeccion, el proceso se somete a cambios de manera constante y debe adaptarse
continuamente para mejroar su proceso y producto

*/

/* 
Ventajas:
-Mejor Flexibilidad y adaptacion a cambios de requisitos
-Entregas regulares de incremento del producto
-Colaboracion estrecha con los stakeholders (Mas vale, si son los que pagan)
-Enfoque en la satisfaccion del cliente.

*/